import { CheckCircle, AlertTriangle, XCircle, Sparkles, ThumbsUp, ThumbsDown } from 'lucide-react';
import { Section } from './OverviewSection';
import type { LLMSummary } from '@/data/evaluationReportData';

interface SummarySectionProps {
  summary: LLMSummary;
}

const verdictConfig = {
  PASS: { icon: CheckCircle, color: '#16a34a', bg: '#f0fdf4', label: '합격 (PASS)' },
  CONDITIONAL_PASS: { icon: AlertTriangle, color: '#ca8a04', bg: '#fffbeb', label: '조건부 합격' },
  FAIL: { icon: XCircle, color: '#dc2626', bg: '#fef2f2', label: '불합격 (FAIL)' },
};

export function SummarySection({ summary }: SummarySectionProps) {
  const v = verdictConfig[summary.verdict];
  const VerdictIcon = v.icon;

  return (
    <Section number="6" title="종합 평가 소견">
      <div className="flex flex-col gap-8">
        {/* Featured Summary Card */}
        <div
          className="bg-white rounded-[8px] p-8 flex flex-col gap-6"
          style={{
            boxShadow: `
              rgba(0,0,0,0.08) 0px 0px 0px 1px,
              rgba(0,0,0,0.04) 0px 2px 2px,
              rgba(0,0,0,0.04) 0px 8px 8px -8px,
              #fafafa 0px 0px 0px 1px
            `,
          }}
        >
          {/* Header */}
          <div className="flex items-start justify-between gap-8">
            <div className="flex items-center gap-3">
              <Sparkles size={20} strokeWidth={1.5} className="text-[#0072f5]" />
              <div className="flex flex-col gap-1">
                <span className="font-mono text-[12px] font-medium uppercase tracking-wider text-[#666666]">
                  AI-Generated Summary · Reviewer-verified
                </span>
                <span className="text-[24px] font-semibold tracking-[-0.06rem] text-[#171717]">
                  종합 의견
                </span>
              </div>
            </div>
            <div
              className="flex items-center gap-3 rounded-[6px] px-4 py-3"
              style={{ backgroundColor: v.bg, borderLeft: `3px solid ${v.color}` }}
            >
              <VerdictIcon size={18} strokeWidth={1.5} style={{ color: v.color }} />
              <div className="flex flex-col">
                <span className="font-mono text-[11px] font-medium uppercase tracking-wider" style={{ color: v.color }}>
                  최종 판정
                </span>
                <span className="text-[16px] font-semibold" style={{ color: v.color }}>
                  {v.label}
                </span>
              </div>
            </div>
          </div>

          {/* Full Text */}
          <div
            className="rounded-[6px] px-6 py-5"
            style={{ backgroundColor: '#fafafa', boxShadow: 'rgba(0,0,0,0.06) 0px 0px 0px 1px' }}
          >
            <p className="text-[16px] font-normal leading-relaxed text-[#171717] max-w-[900px]">
              {summary.fullText}
            </p>
          </div>

          {/* Score footer */}
          <div className="flex items-center justify-between pt-2">
            <div className="flex flex-col gap-1">
              <span className="font-mono text-[12px] font-medium uppercase tracking-wider text-[#666666]">
                종합 점수 (Overall Score)
              </span>
              <span className="text-[40px] font-semibold leading-none tracking-[-0.08rem] text-[#171717]">
                {(summary.overallScore * 100).toFixed(2)}<span className="text-[20px] text-[#666666]">%</span>
              </span>
            </div>
            <div className="flex items-center gap-2">
              <span className="font-mono text-[12px] font-medium uppercase tracking-wider text-[#666666]">
                Generated by
              </span>
              <span
                className="font-mono text-[12px] font-medium px-[10px] py-[2px] rounded-full"
                style={{ backgroundColor: '#ebf5ff', color: '#0068d6' }}
              >
                ISO-4213 Auto Evaluator v1.2
              </span>
            </div>
          </div>
        </div>

        {/* Strengths & Weaknesses */}
        <div className="grid grid-cols-2 gap-8">
          <BulletPanel
            title="강점 (Strengths)"
            icon={<ThumbsUp size={16} strokeWidth={1.5} className="text-[#16a34a]" />}
            color="#16a34a"
            bullets={summary.strengths}
          />
          <BulletPanel
            title="개선점 (Weaknesses)"
            icon={<ThumbsDown size={16} strokeWidth={1.5} className="text-[#ca8a04]" />}
            color="#ca8a04"
            bullets={summary.weaknesses}
          />
        </div>
      </div>
    </Section>
  );
}

// ─── Bullet Panel ─────────────────────────────────────────────────────────

function BulletPanel({
  title,
  icon,
  color,
  bullets,
}: {
  title: string;
  icon: React.ReactNode;
  color: string;
  bullets: string[];
}) {
  return (
    <div
      className="bg-white rounded-[8px] p-6 flex flex-col gap-4"
      style={{ boxShadow: 'rgba(0,0,0,0.08) 0px 0px 0px 1px' }}
    >
      <div className="flex items-center gap-2">
        {icon}
        <span className="text-[16px] font-semibold tracking-[-0.02rem] text-[#171717]">
          {title}
        </span>
      </div>
      <ul className="flex flex-col gap-3">
        {bullets.map((b, i) => (
          <li key={i} className="flex items-start gap-3">
            <span
              className="font-mono text-[12px] font-medium px-2 py-0.5 rounded-[4px] mt-0.5 shrink-0"
              style={{ backgroundColor: `${color}14`, color }}
            >
              {String(i + 1).padStart(2, '0')}
            </span>
            <span className="text-[14px] font-normal leading-relaxed text-[#4d4d4d]">
              {b}
            </span>
          </li>
        ))}
      </ul>
    </div>
  );
}
